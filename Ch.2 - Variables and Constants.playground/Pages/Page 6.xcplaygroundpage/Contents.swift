//: # Defining Floating Points


/*:
### Floating Points
* **Float**: ~ 32-bit 6 decimal place precision
* **Double**: ~ 64-bit 15 decimal place position
* Automatically inferred as double
*/

let doubleNum = 10.0
let floatNum: Float = 10.0
let intNum = 10

doubleNum * Double(floatNum) * Double(intNum)

