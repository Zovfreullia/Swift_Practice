//: # Defining Type Aliases


/*:
### Type Aliases

Create an alias for a type for convenience.

*/

typealias Names = String

let sarah: Names = "Sarah"

let kaira: Names = "Kaira"

typealias IPOctet = UInt8

let minOctetVal = IPOctet.min

let maxOctetVal = IPOctet.max



